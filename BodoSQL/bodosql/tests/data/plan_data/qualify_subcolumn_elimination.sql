select
    A,
    C,
    E,
    MEDIAN(A)
        OVER (
            PARTITION BY C ORDER BY E ASC ROWS BETWEEN UNBOUNDED PRECEDING AND 1 PRECEDING
        ) as WINDOW_AGG_ASC
FROM table1
QUALIFY
    WINDOW_AGG_ASC > 1 OR MEDIAN(A) OVER (
        PARTITION BY C ORDER BY E DESC ROWS BETWEEN UNBOUNDED PRECEDING AND 1 PRECEDING) >= 1
ORDER BY C, E
